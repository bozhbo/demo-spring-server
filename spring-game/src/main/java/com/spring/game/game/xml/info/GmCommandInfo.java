package com.snail.webgame.game.xml.info;

public class GmCommandInfo {

	private int no;// 编号
	private String cmd;// 命令
	private int argNum;// 参数个数

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getCmd() {
		return cmd;
	}

	public void setCmd(String cmd) {
		this.cmd = cmd;
	}

	public int getArgNum() {
		return argNum;
	}

	public void setArgNum(int argNum) {
		this.argNum = argNum;
	}

}
