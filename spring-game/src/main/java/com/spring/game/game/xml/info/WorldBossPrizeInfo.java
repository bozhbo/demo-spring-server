package com.snail.webgame.game.xml.info;

public class WorldBossPrizeInfo {

	private int no;
	private int MinPlace;
	private int MaxPlace;
	private String PlaceDropNo;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getMinPlace() {
		return MinPlace;
	}
	public void setMinPlace(int minPlace) {
		MinPlace = minPlace;
	}
	public int getMaxPlace() {
		return MaxPlace;
	}
	public void setMaxPlace(int maxPlace) {
		MaxPlace = maxPlace;
	}
	public String getPlaceDropNo() {
		return PlaceDropNo;
	}
	public void setPlaceDropNo(String placeDropNo) {
		PlaceDropNo = placeDropNo;
	}
	
}
